const letterSpacing = " "

// RenderASCII renders text as ASCII art. Returns 6 lines.
func RenderASCII(text string) string {
	text = strings.ToUpper(text)

	lines := [6]string{}
	for _, ch := range text {
		glyph, ok := font[ch]
		if !ok {
			glyph = font[' ']
		}
		for i := range 6 {
			if lines[i] != "" {
				lines[i] += letterSpacing
			}
			lines[i] += glyph[i]
		}
	}

	return strings.Join(lines[:], "\n")
}

// ASCIIWidth returns the rendered width of ASCII text.
func ASCIIWidth(text string) int {
	text = strings.ToUpper(text)
	width := 0
	for i, ch := range text {
		glyph, ok := font[ch]
		if !ok {
			glyph = font[' ']
		}
		width += len([]rune(glyph[0]))
		if i > 0 {
			width += len(letterSpacing)
		}
	}
	return width
}
